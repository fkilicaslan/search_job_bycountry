const SCAN_QUERY = '"Account Executive" OR "Sales Manager" OR "Business Development" OR "Partnerships" OR "Alliance Manager"';

let yaml = '\n  # -- VC Portfolio Companies (added 2026-05-17) --\n';

for (const [name, category, city, domain] of companies) {
  if (ALREADY_PRESENT.has(name)) continue;
  const careersUrl = `https://${domain}/careers`;
  const safeQuery = `'site:${domain} ${SCAN_QUERY}'`;
  yaml += `\n  - name: ${name}\n`;
  yaml += `    careers_url: ${careersUrl}\n`;
  yaml += `    scan_method: websearch\n`;
  yaml += `    scan_query: ${safeQuery}\n`;
  yaml += `    notes: "${category}. ${city}."\n`;
  yaml += `    enabled: true\n`;
}

// Read portals.yml and insert before board_searches
const portalsPath = 'portals.yml';
const content = readFileSync(portalsPath, 'utf-8');
const insertBefore = '\n# -- Board searches (scan-boards.mjs) --';
const idx = content.indexOf(insertBefore);
if (idx === -1) { console.error('Could not find insertion point'); process.exit(1); }

const updated = content.slice(0, idx) + yaml + content.slice(idx);
writeFileSync(portalsPath, updated, 'utf-8');

const count = (yaml.match(/- name:/g) || []).length;
console.log(`Done. Added ${count} companies to portals.yml.`);
